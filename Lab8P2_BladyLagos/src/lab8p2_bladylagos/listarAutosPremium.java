    public void listarPremium() {
        int cantidad = GestionVehiculos.listavehiculos.size();
        DefaultTableModel modelo = (DefaultTableModel) tabpremium.getModel();
        tabpremium.setModel(modelo);
        String[] datos = new String[3];
        for (int i = 0; i < cantidad; i++) {
            if (GestionVehiculos.listavehiculos.get(i).getMarca() == "Audi" || GestionVehiculos.listavehiculos.get(i).getMarca() == "Volkswagen"
                    || GestionVehiculos.listavehiculos.get(i).getMarca() == "Bentley") {
                datos[0] = GestionVehiculos.listavehiculos.get(i).getMarca();
                datos[1] = GestionVehiculos.listavehiculos.get(i).getVin();
                datos[2] = GestionVehiculos.listavehiculos.get(i).getCarroceria();
                modelo.addRow(datos);
            }

        }
    }

